export function getMeetingById(id: string): Meeting | undefined {
  return mockMeetings.find(m => m.id === id);
}

export function getSpeakersByMeetingId(meetingId: string): Speaker[] {
  return mockSpeakers.filter(s => s.meetingId === meetingId);
}

export function getTranscriptsByMeetingId(meetingId: string): TranscriptSegment[] {
  return mockTranscripts.filter(t => t.meetingId === meetingId).sort((a, b) => a.order - b.order);
}

export function getTopicsByMeetingId(meetingId: string): Topic[] {
  return mockTopics.filter(t => t.meetingId === meetingId).sort((a, b) => a.order - b.order);
}
